// ¿Qué es el DOM?
// Estructura del Documento: El DOM presenta el documento HTML como una jerarquía de nodos. 
// Cada nodo representa una parte del documento, como un elemento (por ejemplo, <div>, <p>, <a>), 
//     un atributo (por ejemplo, class, id), o un texto dentro de un elemento.

// Acceso y Manipulación: JavaScript puede acceder al DOM para leer y modificar el contenido del documento. 
// Por ejemplo, puedes cambiar el texto de un párrafo, modificar atributos, añadir o eliminar elementos, etc.

// Eventos: El DOM también permite manejar eventos, como clics del usuario, movimientos del ratón, 
// teclado, etc., y ejecutar funciones JavaScript en respuesta a esos eventos.

